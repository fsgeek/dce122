...\"#doctitle
.I "OSF Style Guide"
was developed to help technical writers take a
unified approach to providing industry-standard documentation
for OSF and its licensees. This
style guide provides a single set of writing style and process
guidelines. Adherence to these guidelines will help ensure that
OSF's documentation, and that of OSF's third-party vendors is
consistent in style, organization, terminology, and appearance.
.P
Considerations of internationalization, online documentation,
help, and human factors are interspersed throughout this style
guide, rather than appearing in discrete appendixes. In this
way, we hope to make this information more useable and more
readily available.
