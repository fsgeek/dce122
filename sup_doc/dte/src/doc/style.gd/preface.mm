.I "OSF Style Guide" 
is organized as follows:
.VL 1i
.LI
.B "Chapter 1: Knowing Your Readers"
.br
Discusses the importance of understanding
both your readers' needs and the product you are documenting
to the development of useful documentation.
.LI
.B "Chapter 2: Structural Elements for OSF Documentation"
.br
Chapter 2 provides the specifications for the
structural elements required in OSF documentation
offerings, and the order in which the information is
to appear.
.LI
.B "Chapter 3: Usage Guidelines"
.br
Chapter 3 provides guidelines for capitalization, spelling,
punctuation, documentation conventions, glossaries, and
indexing. Topics are arranged in alphabetical order
by main topic.
.LI
.B "Chapter 4: Word List"
.br
Chapter 4 lists commonly used words, with preferred
spellings, 1-word and 2-word preferences, and
guidelines for word use.
.LI
.B "Chapter 5: Abbreviations, Signs, and Symbols"
.br
This chapter is for reference. It contains abbreviations for
units of measure and commonly used technical phrases. It
also contains commonly used signs and symbols.
.LI
.B "Appendix A: Recommended Reference Books"
.br
Appendix A lists standard reference books that can be used
to resolve questions not covered in this guide.
